class ReportGenerator:
    """
    å®Ÿé¨“ãƒ¬ãƒãƒ¼ãƒˆè‡ªå‹•ç”Ÿæˆã‚¨ãƒ³ã‚¸ãƒ³
    
    Features:
    - HTMLå½¢å¼ã®ãƒ¬ãƒãƒ¼ãƒˆç”Ÿæˆ
    - ãƒ¡ãƒˆãƒªã‚¯ã‚¹ã€è¨­å®šã€å¯è¦–åŒ–ã®è‡ªå‹•çµ±åˆ
    - PDFå¤‰æ›å¯¾å¿œï¼ˆweasyprintãŒå¿…è¦ï¼‰
    
    Example:
        >>> generator = ReportGenerator()
        >>> html = generator.generate(
        ...     experiment_name="Solubility Prediction",
        ...     metrics={"r2": 0.95, "rmse": 0.15},
        ...     config={"model": "lightgbm", "cv_folds": 5},
        ...     figures=[fig1, fig2],
        ... )
        >>> generator.save_html(html, "report.html")
    """
    
    def __init__(self, output_dir: str = "./reports"):
        self.output_dir = output_dir
        os.makedirs(output_dir, exist_ok=True)
    
    def generate(
        self,
        experiment_name: str,
        metrics: Dict[str, float],
        config: Dict[str, Any],
        figures: Optional[List[plt.Figure]] = None,
        figure_captions: Optional[List[str]] = None,
        feature_names: Optional[List[str]] = None,
        feature_importances: Optional[Dict[str, float]] = None,
        model_type: str = "Unknown",
    ) -> str:
        """
        HTMLãƒ¬ãƒãƒ¼ãƒˆã‚’ç”Ÿæˆ
        
        Args:
            experiment_name: å®Ÿé¨“å
            metrics: ãƒ¡ãƒˆãƒªã‚¯ã‚¹è¾æ›¸
            config: è¨­å®šè¾æ›¸
            figures: Matplotlibãƒ•ã‚£ã‚®ãƒ¥ã‚¢ãƒªã‚¹ãƒˆ
            figure_captions: ãƒ•ã‚£ã‚®ãƒ¥ã‚¢ã®ã‚­ãƒ£ãƒ—ã‚·ãƒ§ãƒ³
            feature_names: ç‰¹å¾´é‡åãƒªã‚¹ãƒˆ
            feature_importances: ç‰¹å¾´é‡é‡è¦åº¦
            model_type: ãƒ¢ãƒ‡ãƒ«ã‚¿ã‚¤ãƒ—
            
        Returns:
            HTMLæ–‡å­—åˆ—
        """
        # ãƒ¡ãƒˆãƒªã‚¯ã‚¹HTML
        metrics_html = self._generate_metrics_html(metrics)
        
        # è¨­å®šHTML
        config_html = self._generate_config_html(config)
        
        # ãƒ•ã‚£ã‚®ãƒ¥ã‚¢HTML
        figures_html = self._generate_figures_html(figures, figure_captions)
        
        # ç‰¹å¾´é‡HTML
        features_html = self._generate_features_html(feature_names, feature_importances)
        
        html = HTML_TEMPLATE.format(
            title=f"å®Ÿé¨“ãƒ¬ãƒãƒ¼ãƒˆ: {experiment_name}",
            generated_at=datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            experiment_name=experiment_name,
            model_type=model_type,
            metrics_html=metrics_html,
            config_html=config_html,
            figures_html=figures_html,
            features_html=features_html,
        )
        
        return html
    
    def _generate_metrics_html(self, metrics: Dict[str, float]) -> str:
        """ãƒ¡ãƒˆãƒªã‚¯ã‚¹ã‚«ãƒ¼ãƒ‰HTML"""
        cards = []
        for name, value in metrics.items():
            cards.append(f"""
                <div class="metric-card">
                    <div class="metric-value">{value:.4f}</div>
                    <div class="metric-name">{name}</div>
                </div>
            """)
        return "\n".join(cards)
    
    def _generate_config_html(self, config: Dict[str, Any]) -> str:
        """è¨­å®šãƒ†ãƒ¼ãƒ–ãƒ«HTML"""
        rows = []
        for key, value in config.items():
            rows.append(f"<tr><td>{key}</td><td>{value}</td></tr>")
        return "\n".join(rows)
    
    def _generate_figures_html(
        self,
        figures: Optional[List[plt.Figure]],
        captions: Optional[List[str]],
    ) -> str:
        """ãƒ•ã‚£ã‚®ãƒ¥ã‚¢HTML"""
        if not figures:
            return "<p>å¯è¦–åŒ–ãªã—</p>"
        
        html_parts = []
        captions = captions or [f"Figure {i+1}" for i in range(len(figures))]
        
        for fig, caption in zip(figures, captions):
            try:
                buf = io.BytesIO()
                fig.savefig(buf, format='png', bbox_inches='tight', dpi=150)
                buf.seek(0)
                img_b64 = base64.b64encode(buf.getvalue()).decode('utf-8')
                
                html_parts.append(f"""
                    <div class="figure">
                        <img src="data:image/png;base64,{img_b64}" alt="{caption}">
                        <div class="figure-caption">{caption}</div>
                    </div>
                """)
            except Exception as e:
                logger.warning(f"Figure conversion failed: {e}")
        
        return "\n".join(html_parts)
    
    def _generate_features_html(
        self,
        feature_names: Optional[List[str]],
        importances: Optional[Dict[str, float]],
    ) -> str:
        """ç‰¹å¾´é‡ãƒ†ãƒ¼ãƒ–ãƒ«HTML"""
        if not feature_names and not importances:
            return "<p>ç‰¹å¾´é‡æƒ…å ±ãªã—</p>"
        
        if importances:
            sorted_imp = sorted(importances.items(), key=lambda x: x[1], reverse=True)[:20]
            rows = [
                f"<tr><td>{name}</td><td>{imp:.4f}</td></tr>"
                for name, imp in sorted_imp
            ]
            return f"""
                <table>
                    <tr><th>ç‰¹å¾´é‡</th><th>é‡è¦åº¦</th></tr>
                    {"".join(rows)}
                </table>
            """
        
        if feature_names:
            return f"<p>ä½¿ç”¨ç‰¹å¾´é‡: {len(feature_names)}ä»¶</p><p>{', '.join(feature_names[:20])}...</p>"
        
        return ""
    
    def save_html(self, html: str, filename: str) -> str:
        """HTMLã‚’ä¿å­˜"""
        filepath = os.path.join(self.output_dir, filename)
        with open(filepath, "w", encoding="utf-8") as f:
            f.write(html)
        logger.info(f"Report saved: {filepath}")
        return filepath
    
    def save_pdf(self, html: str, filename: str) -> Optional[str]:
        """PDFã‚’ä¿å­˜ï¼ˆweasyprintå¿…è¦ï¼‰"""
        try:
            from weasyprint import HTML
            
            filepath = os.path.join(self.output_dir, filename)
            HTML(string=html).write_pdf(filepath)
            logger.info(f"PDF saved: {filepath}")
            return filepath
            
        except ImportError:
            logger.warning("weasyprint required for PDF. Run: pip install weasyprint")
            return None
